def rootlengths(n, **kwargs):
    """
    Returns a generator giving the relative root lengths.

    """
    return (1 for _ in range(n))

def diagram(inds):
    """Prints the Dynkin diagram."""
    sinds = list(map(str, inds))
    out1 = "E{} :  {} - {} - ".format(len(inds), sinds[0], sinds[2])
    out1 += " - ".join(sinds[3:])
    out2 = "      " + " "*(6 + len(sinds[0]) + len(sinds[2]))

    print(out1, out2 + "|", out2 + sinds[1], sep="\n")
    return None

def degrees(n):
    if n == 6:
        return [2, 5, 6, 8, 9, 12]
    if n == 7:
        return [2, 6, 8, 10, 12, 14, 18]
    if n == 8:
        return [2, 8, 12, 14, 18, 20, 24, 30]

def longestword(inds):
    n = len(inds)
    if n == 6:
        return [inds[k] for k in [0, 3, 5, 1, 2, 4]]*6
    if n == 7:
        return [inds[k] for k in [0, 3, 5, 1, 2, 4, 6]]*9
    if n == 8:
        return [inds[k] for k in [0, 3, 5, 7, 1, 2, 4, 6]]*15

def coxeterclasses(inds):
    n = len(inds)
    if n == 6:
        return (
            (nam, orblen, [inds[i] for i in rep])
            for (nam, orblen, rep) in _rd.E6coxeterclasses
        )
    if n == 7:
        return (
            (nam, orblen, [inds[i] for i in rep])
            for (nam, orblen, rep) in _rd.E7coxeterclasses
        )
    if n == 8:
        return (
            (nam, orblen, [inds[i] for i in rep])
            for (nam, orblen, rep) in _rd.E8coxeterclasses
        )

