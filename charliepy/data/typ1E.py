def irrchars(n, labels=(), **kwargs):
    if n == 6:
        if "E6frame" in labels or "lusztig" in labels:
            return (
                ('{d}{p}'.format(d=deg, p=frame), a, b)
                for (deg, a, b, frame) in _rd.E6irrchardata
            )
        else:
            return (
                ('phi{{{d},{b}}}'.format(d=deg, b=b), a, b)
                for (deg, a, b, frame) in _rd.E6irrchardata
            )
    if n == 7:
        if "E7frame" in labels or "lusztig" in labels:
            return (
                ('{d}{p}'.format(d=deg, p=frame), a, b)
                for (deg, a, b, frame) in _rd.E7irrchardata
            )
        else:
            return (
                ('phi{{{d},{b}}}'.format(d=deg, b=b), a, b)
                for (deg, a, b, frame) in _rd.E7irrchardata
            )
    if n == 8:
        if "E8frame" in labels or "lusztig" in labels:
            return (
                ('{d}{p}'.format(d=deg, p=frame), a, b)
                for (deg, a, b, frame) in _rd.E8irrchardata
            )
        else:
            return (
                ('phi{{{d},{b}}}'.format(d=deg, b=b), a, b)
                for (deg, a, b, frame) in _rd.E8irrchardata
            )

