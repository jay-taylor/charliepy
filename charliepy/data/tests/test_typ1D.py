class TestDData(unittest.TestCase):

    def test_chartabs(self):
        # Make sure the character tables agree with those produced by CHEVIE.
        for i in range(2, 9):
            ctab = typ1D.chartable(i)

            for j, mu in enumerate(typ1D._charlabels(i)):
                for k, nu in enumerate(typ1D._conjlabels(i)):
                    mup = mu[:]
                    # We have to swap round the +/- labels when n is congruent
                    # to 2 mod 4.
                    if i % 4:
                        if mu[1] == '+':
                            mup[1] = '-'
                        elif mu[1] == '-':
                            mup[1] = '+'
                    try:
                        x = data[i]['chars'][0].index(mup)
                    except ValueError:
                        x = data[i]['chars'][0].index(mup[::-1])
                    y = data[i]['class'][0].index(nu)
                    self.assertTrue(ctab[j][k] == data[i]['ctab'][x][y],
                        'Rank ' + str(i) + ' character table not correct!'
                        + '\n' + str(j) + str(mu) + '\n' + str(j) + str(nu))

        # Test that the character tables are internally consistent.
        for i in range(2, 10):
            ctab = typ1D.chartable(i)
            cents = typ1D.conjclassdata(list(range(1, i+1)))[1]
            size = math.factorial(i)*2**(i-1)
            classlens = [size//c for c in cents]

            # Row orthogonality relations.
            self.assertTrue(
                np.array_equal(
                    size*np.eye(ctab.shape[0], dtype='int'),
                    np.inner(ctab, ctab*classlens)
                ),
                'Rank ' + str(i) + ' character table not consistent!'
            )

            # Column orthogonality relations.
            self.assertTrue(
                np.array_equal(
                    cents*np.eye(ctab.shape[0], dtype='int'),
                    np.inner(ctab.T, ctab.T)
                ),
                'Rank ' + str(i) + ' character table not consistent!'
            )

    def test_conjclasses(self):
        # Make sure the conjugacy class information is being produced
        # correctly.
        for i in range(2, 9):
            classes = typ1D.conjclassdata(list(range(1, i+1)))

            for ind, label in enumerate(typ1D._conjlabels(i)):
                # Look up the conjugacy class labeled by bpart in the list
                # above and then check the data matches.
                x = data[i]['class'][0].index(label)

                self.assertEqual(data[i]['class'][1][x], classes[0][ind],
                    'Rank ' + str(i) + ' conjugacy class representatives are '
                    + 'not correct! ')
                self.assertEqual(data[i]['class'][2][x], classes[1][ind],
                    'Rank ' + str(i) + ' centraliser orders are not correct! '
                    + str(label))
                self.assertEqual(data[i]['class'][3][x], classes[2][ind],
                    'Rank ' + str(i) + ' conjugacy class names are '
                    'not correct!')

    def test_characters(self):
        # Make sure the conjugacy class information is being produced
        # correctly.
        for i in range(2, 9):
            chars = typ1D.irrchardata(i)

            for ind, label in enumerate(typ1D._charlabels(i)):
                try:
                    x = data[i]['chars'][0].index(label)
                    nam = chars[0][ind]
                except ValueError:
                    x = data[i]['chars'][0].index(label[::-1])
                    nam = ".".join(chars[0][ind].split(".")[::-1])

                self.assertEqual(data[i]['chars'][1][x], nam,
                    'Rank ' + str(i) + ' character names are not correct!')
                self.assertEqual(data[i]['chars'][2][x], chars[1][ind],
                    'Rank ' + str(i) + ' a-values are not correct!')
                self.assertEqual(data[i]['chars'][3][x], chars[2][ind],
                    'Rank ' + str(i) + ' b-values are not correct!')

